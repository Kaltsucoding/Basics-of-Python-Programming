# Kysytään käyttäjältä kaksi lukua ja tehdään niille laskutoimituksia
# Summa
Ensimmäinen = int(input("Anna ensimmäinen luku "))
Toinen = int(input("Anna toinen luku "))
Vastaus = Ensimmäinen + Toinen
print(Vastaus)

# Erotus
Ensimmäinen = int(input("Anna ensimmäinen luku "))
Toinen = int(input("Anna toinen luku "))
